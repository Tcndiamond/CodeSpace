x = 3
y = 10

#Addition
answer = x + y
print(f'{x} + {y} = {answer}')

#Positive Subtraction
answer = y - x
print(f'{y} - {x} = {answer}')

#Negative Subtraction
answer = x - y
print(f'{x} - {y} = {answer}')

#Multiplication
answer = x * y
print(f'{x} * {y} = {answer}')

#Division
answer = y / x
print(f'{y} / {x} = {answer}')

#Modulus
answer = y % x
print(f'{y} % {x} = {answer}')

#Exponentiation
answer = y ** x
print(f'{x} ** {y} = {answer}')

#floor division
answer = y // x
print(f'{y} // {x} = {answer}')
